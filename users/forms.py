class _UserBaseForm(forms.ModelForm):
    """
    Reusable base for all user forms.

    KEY CHANGE: the 'password' widget is TextInput (not PasswordInput).
    PasswordInput has render_value=False by default, which makes Django
    discard the value in POST so it arrives empty in cleaned_data. Since the
    temporary password is visible in the form anyway, using TextInput is
    correct and solves the validation issue.

    The field is readonly in the template; its value is generated by frontend JS.
    """

    role = forms.ChoiceField(
        label='Rol',
        choices=[
            (UserCompany.RoleChoices.EMPLOYEE, 'Empleado'),
            (UserCompany.RoleChoices.MANAGER,  'Manager'),
        ],
        widget=forms.Select(attrs={'class': 'form-control'}),
        required=False,
    )

    class Meta:
        model  = Users
        fields = ['username', 'surname', 'dni', 'email', 'status', 'password']
        labels = {
            'username': 'Nombre',
            'surname':  'Apellidos',
            'dni':      'DNI',
            'email':    'Correo electrГіnico',
            'status':   'Estado',
            'password': 'ContraseГұa temporal',
        }
        widgets = {
            'username': forms.TextInput(attrs={'class': 'form-control'}),
            'surname':  forms.TextInput(attrs={'class': 'form-control'}),
            'dni':      forms.TextInput(attrs={'class': 'form-control'}),
            'email':    forms.EmailInput(attrs={'class': 'form-control'}),
            'status':   forms.Select(attrs={'class': 'form-control'}),
            # TextInput instead of PasswordInput: prevents Django from dropping
            # the field value while processing POST.
            'password': forms.TextInput(attrs={
                'class':    'form-control font-monospace',
                'readonly': True,
            }),
        }

    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        for field in self.fields.values():
            field.required = False
        self.fields['dni'].required = True

    def save(self, commit=True):
        user     = super().save(commit=False)
        password = self.cleaned_data.get('password')
        if password:
            user.set_password(password)
        user.is_admin = False
        if commit:
            user.save()
        return user


