class SetPasswordForm(forms.Form):
    """
    Shown to users on their first login (must_change_password=True).
    Requires setting a final password with complexity requirements.
    """
    new_password = forms.CharField(
        label='Nueva contraseГұa',
        widget=forms.PasswordInput(attrs={
            'class':        'form-control',
            'autocomplete': 'new-password',
            'placeholder':  'MГӯnimo 8 caracteres',
        }),
        min_length=8,
    )
    confirm_password = forms.CharField(
        label='Confirmar contraseГұa',
        widget=forms.PasswordInput(attrs={
            'class':        'form-control',
            'autocomplete': 'new-password',
        }),
    )

    def clean(self):
        cleaned_data     = super().clean()
        new_password     = cleaned_data.get('new_password', '')
        confirm_password = cleaned_data.get('confirm_password', '')

        if new_password != confirm_password:
            self.add_error('confirm_password', 'Las contraseГұas no coinciden.')

        if new_password:
            has_upper = any(c.isupper() for c in new_password)
            has_lower = any(c.islower() for c in new_password)
            has_digit = any(c.isdigit() for c in new_password)
            special_chars = '!ВЎВҝ?@#$%^&*-_;:./~'
            has_special = any(c in special_chars for c in new_password)
            if not (has_upper and has_lower and has_digit and has_special):
                self.add_error(
                    'new_password',
                    'La contraseГұa debe contener mayГәsculas, minГәsculas, nГәmeros y sГӯmbolos especiales (!ВЎВҝ?@#$%^&*-_;:./~).',
                )
        return cleaned_data


